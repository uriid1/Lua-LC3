.ORIG x3000
    LD R0, NUM      ; Загружаем число в R0
    ADD R0, R0, #1  ; Увеличиваем число на 1
    OUT             ; Выводим ASCII-символ (число интерпретируется как код символа)
    HALT            ; Остановка программы
NUM .FILL x0041     ; Исходное число (x41 = 'A', x42 = 'B' после увеличения)
.END
