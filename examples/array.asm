.ORIG x3000
  ; Загрузить адрес начала массива в R4
  LEA R4, ARRAY       ; R4 ← адрес метки ARRAY
  ; Загрузить второй элемент массива (смещение +1) в R1
  LDR R1, R4, #1      ; R1 ← M[R4 + 1]
  ; Увеличить загруженное значение на 1
  ADD R1, R1, #1      ; R1 ← R1 + 1
  ; Завершить выполнение программы
  HALT

; Область данных: массив чисел
ARRAY
  .FILL x0005         ; Первый элемент массива (индекс 0)
  .FILL x000A         ; Второй элемент массива (индекс 1) – значение для LDR
  .FILL x0000         ; Третий элемент массива (не используется)

.END
